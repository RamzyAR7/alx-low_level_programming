#include "3-calc.h"
/**
 * op_add - to get the sum
 * @a: n1
 * @b:n2
 * Return: the sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - to get the sub
 * @a: n1
 * @b:n2
 * Return: the sub of a and b
 */
int op_sub(int a, int b)
{

	return (a - b);
}
/**
 * op_mul - to get the mul
 * @a: n1
 * @b:n2
 * Return: the mul of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - to get the div
 * @a: n1
 * @b:n2
 * Return: the div of a and b
 */
int op_div(int a, int b)
{

	return (a / b);
}
/**
 * op_mod - to get the mod
 * @a: n1
 * @b:n2
 * Return: the mod of a and b
 */
int op_mod(int a, int b)
{

	return (a % b);
}
